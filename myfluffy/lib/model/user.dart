
class User {
  final String bankAccount;
  final String email;
  final String location;
  final String name;
  final String password;
  final String phone;
  final String userId;

  User({
    required this.bankAccount,
    required this.email,
    required this.location,
    required this.name,
    required this.password,
    required this.phone,
    required this.userId,
  });
}